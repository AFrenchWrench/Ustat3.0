import json
import psycopg2


# Function to load JSON data from file
def load_json(filename):
    with open(filename, encoding="utf-8") as file:
        return json.load(file)


# Function to insert data into the database
def insert_data(conn, table_name, data, *args):
    cur = conn.cursor()
    for item in data:
        # Create a tuple of values based on provided arguments
        values = tuple(item[arg] for arg in args)
        # Generate the SQL query dynamically based on the number of variables
        placeholders = ", ".join(["%s"] * len(args))
        query = f"INSERT INTO {table_name} ({', '.join(args)}) VALUES ({placeholders})"
        # Execute the query
        cur.execute(query, values)
    conn.commit()
    cur.close()


